    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            
            txtAT.setText(model.getValueAt(row, 0).toString());
            
            // 2. Επιλέγουμε αυτόματα την τιμή στο Dropdown
            String typeFromTable = model.getValueAt(row, 1).toString();
            cmbType.setSelectedItem(typeFromTable); 
            
            txtDiploma.setText(model.getValueAt(row, 2).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String at = txtAT.getText().trim();
        String type = cmbType.getSelectedItem().toString(); 
        String diploma = txtDiploma.getText().trim();
        
        if(at.isEmpty() || diploma.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Fill AT and Diploma!");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "INSERT INTO admin (adm_AT, adm_type, adm_diploma) VALUES (?, ?, ?)")) {
            
            pst.setString(1, at);
            pst.setString(2, type); // Στέλνουμε την επιλογή
            pst.setString(3, diploma);
            
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Admin Added!");
            loadData();
            txtAT.setText(""); txtDiploma.setText("");
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String at = txtAT.getText().trim();
        String type = cmbType.getSelectedItem().toString();
        String diploma = txtDiploma.getText().trim();
        
        if(at.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select an admin to update.");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "UPDATE admin SET adm_type=?, adm_diploma=? WHERE adm_AT=?")) {
            
            pst.setString(1, type);
            pst.setString(2, diploma);
            pst.setString(3, at);
            
            int rows = pst.executeUpdate();
            
            if (rows > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Updated!");
                loadData();
                txtAT.setText(""); txtDiploma.setText("");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Admin AT not found!");
            }
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String at = txtAT.getText();
        
        if(at.isEmpty()) {
             javax.swing.JOptionPane.showMessageDialog(this, "Select an admin to delete.");
             return;
        }
        
        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement("DELETE FROM admin WHERE adm_AT=?")) {
            
            pst.setString(1, at);
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Deleted!");
            loadData();
            txtAT.setText(""); txtDiploma.setText("");
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

