    private void loadData(){                                         
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        model.setColumnIdentifiers(new Object[]{"Admin AT", "Type", "Diploma"});

        try {
            java.sql.Connection con = DBConnection.connect();
            String sql = "SELECT * FROM admin"; 
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String col1 = rs.getString("adm_AT"); 
                String col2 = rs.getString("adm_type"); 
                String col3 = rs.getString("adm_diploma"); 
                
                model.addRow(new Object[]{col1, col2, col3});
            }
            con.close(); 
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
