    private void fillCombos() {
        try (Connection con = DBConnection.connect(); Statement st = con.createStatement()) {
            // Γέμισμα Ταξιδιών (Trip)
            cmbTrip.removeAllItems();
            ResultSet rs1 = st.executeQuery("SELECT tr_id, tr_departure FROM trip");
            while (rs1.next()) {
                String label = "Trip #" + rs1.getInt("tr_id") + " (" + rs1.getTimestamp("tr_departure") + ")";
                tripMap.put(label, rs1.getInt("tr_id"));
                cmbTrip.addItem(label);
            }

            // Γέμισμα Πελατών (Customer)
            cmbCust.removeAllItems();
            ResultSet rs2 = st.executeQuery("SELECT cust_id, cust_name, cust_lname FROM customer");
            while (rs2.next()) {
                String label = rs2.getString("cust_name") + " " + rs2.getString("cust_lname");
                custMap.put(label, rs2.getInt("cust_id"));
                cmbCust.addItem(label);
            }
            
            // Γέμισμα Status (ENUM)
            cmbStatus.setModel(new DefaultComboBoxModel<>(new String[] { "PENDING", "CONFIRMED", "PAID", "CANCELLED" }));
        } catch (Exception e) { e.printStackTrace(); }
    }

    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{"Trip ID", "Seat #", "Cust ID", "Status", "Cost"});
        
        try (Connection con = DBConnection.connect(); Statement st = con.createStatement(); 
             ResultSet rs = st.executeQuery("SELECT * FROM reservation")) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("res_tr_id"), rs.getInt("res_seatnum"),
                    rs.getInt("res_cust_id"), rs.getString("res_status"),
                    rs.getBigDecimal("res_total_cost")
                });
            }
        } catch (Exception e) { e.printStackTrace(); }
    }
    
    private void selectComboItemById(JComboBox combo, HashMap<String, Integer> map, int id) {
        for (String label : map.keySet()) {
            if (map.get(label) == id) {
                combo.setSelectedItem(label);
                return;
            }
        }
    }
    
