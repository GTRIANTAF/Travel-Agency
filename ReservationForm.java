    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int tripId = tripMap.get(cmbTrip.getSelectedItem().toString());
        int seatNum = Integer.parseInt(txtSeat.getText());
        int custId = custMap.get(cmbCust.getSelectedItem().toString());

        try (Connection con = DBConnection.connect()) {
            PreparedStatement checkPst = con.prepareStatement("SELECT tr_maxseats FROM trip WHERE tr_id = ?");
            checkPst.setInt(1, tripId);
            ResultSet rs = checkPst.executeQuery();
            if (rs.next() && seatNum > rs.getInt("tr_maxseats")) {
                JOptionPane.showMessageDialog(this, "Αδύνατη κράτηση! Το ταξίδι έχει μόνο " + rs.getInt("tr_maxseats") + " θέσεις.");
                return;
            }

            String sql = "INSERT INTO reservation (res_tr_id, res_seatnum, res_cust_id, res_status, res_total_cost) VALUES (?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, tripId);
            pst.setInt(2, seatNum);
            pst.setInt(3, custId);
            pst.setString(4, cmbStatus.getSelectedItem().toString());
            pst.setDouble(5, Double.parseDouble(txtCost.getText()));
            
            pst.executeUpdate();
            loadData();
            JOptionPane.showMessageDialog(this, "Η κράτηση έγινε!");
        } catch (Exception e) { 
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Σφάλμα: Ίσως η θέση είναι πιασμένη."); }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (oldTripId == -1) return;
            String sql = "UPDATE reservation SET res_tr_id=?, res_seatnum=?, res_cust_id=?, res_status=?, res_total_cost=? WHERE res_tr_id=? AND res_seatnum=?";
            try (Connection con = DBConnection.connect(); PreparedStatement pst = con.prepareStatement(sql)) {
                pst.setInt(1, tripMap.get(cmbTrip.getSelectedItem().toString()));
                pst.setInt(2, Integer.parseInt(txtSeat.getText()));
                pst.setInt(3, custMap.get(cmbCust.getSelectedItem().toString()));
                pst.setString(4, cmbStatus.getSelectedItem().toString());
                pst.setDouble(5, Double.parseDouble(txtCost.getText()));
                pst.setInt(6, oldTripId);
                pst.setInt(7, oldSeatNum);

                pst.executeUpdate();
                loadData();
                JOptionPane.showMessageDialog(this, "Ενημερώθηκε!");
            } catch (Exception e) { JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     if (oldTripId == -1) return;
        try (Connection con = DBConnection.connect(); 
             PreparedStatement pst = con.prepareStatement("DELETE FROM reservation WHERE res_tr_id=? AND res_seatnum=?")) {
            pst.setInt(1, oldTripId);
            pst.setInt(2, oldSeatNum);
            pst.executeUpdate();
            loadData();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Delete Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            oldTripId = (int) jTable1.getValueAt(row, 0);
            oldSeatNum = (int) jTable1.getValueAt(row, 1);
            
            txtSeat.setText(String.valueOf(oldSeatNum));
            txtCost.setText(jTable1.getValueAt(row, 4).toString());
            cmbStatus.setSelectedItem(jTable1.getValueAt(row, 3).toString());
            // Συγχρονισμός Combos για Trip/Customer
            selectComboItemById(cmbTrip, tripMap, oldTripId);
            selectComboItemById(cmbCust, custMap, (int) jTable1.getValueAt(row, 2));
        }
    }//GEN-LAST:event_jTable1MouseClicked

