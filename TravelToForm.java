    private void fillCombos() {
        try (Connection con = DBConnection.connect(); Statement st = con.createStatement()) {
            
            cmbTrip.removeAllItems();
            ResultSet rs1 = st.executeQuery("SELECT tr_id, tr_departure FROM trip");
            while (rs1.next()) {
                String label = "Trip #" + rs1.getInt("tr_id") + " (" + rs1.getTimestamp("tr_departure") + ")";
                tripMap.put(label, rs1.getInt("tr_id"));
                cmbTrip.addItem(label);
            }

            // Destinations
            cmbDest.removeAllItems();
            ResultSet rs2 = st.executeQuery("SELECT dst_id, dst_name FROM destination");
            while (rs2.next()) {
                String name = rs2.getString("dst_name");
                destMap.put(name, rs2.getInt("dst_id"));
                cmbDest.addItem(name);
            }
        } catch (Exception e) { e.printStackTrace(); }
    }
    
    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        model.setColumnIdentifiers(new Object[]{"Trip ID", "Dest. ID", "Arrival", "Departure", "Sequence"});
        
        String sql = "SELECT * FROM travel_to ORDER BY to_tr_id, to_sequence";
        
        try (Connection con = DBConnection.connect(); Statement st = con.createStatement(); ResultSet rs = st.executeQuery(sql)) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("to_tr_id"),
                    rs.getInt("to_dst_id"),
                    rs.getTimestamp("to_arrival"),
                    rs.getTimestamp("to_departure"),
                    rs.getInt("to_sequence")
                });
            }
        } catch (Exception e) { JOptionPane.showMessageDialog(this, "Load Error: " + e.getMessage()); }
    }
    
    private void selectComboItemById(JComboBox combo, HashMap<String, Integer> map, int id) {
        for (Object item : map.keySet()) {
            if (map.get(item.toString()) == id) {
                combo.setSelectedItem(item);
                return;
            }
        }
    }

