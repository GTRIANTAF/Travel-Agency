    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       String sql = "INSERT INTO travel_to (to_tr_id, to_dst_id, to_arrival, to_departure, to_sequence) VALUES (?, ?, ?, ?, ?)";
        try (Connection con = DBConnection.connect(); PreparedStatement pst = con.prepareStatement(sql)) {
            pst.setInt(1, tripMap.get(cmbTrip.getSelectedItem().toString()));
            pst.setInt(2, destMap.get(cmbDest.getSelectedItem().toString()));
            pst.setString(3, txtArrival.getText()); 
            pst.setString(4, txtDeparture.getText());
            pst.setInt(5, Integer.parseInt(txtSeq.getText()));

            pst.executeUpdate();
            loadData();
            JOptionPane.showMessageDialog(this, "Η στάση προστέθηκε!");
        } catch (Exception e) { JOptionPane.showMessageDialog(this, "Insert Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    if (oldTripId == -1) return;
        String sql = "UPDATE travel_to SET to_tr_id=?, to_dst_id=?, to_arrival=?, to_departure=?, to_sequence=? WHERE to_tr_id=? AND to_dst_id=?";
        try (Connection con = DBConnection.connect(); PreparedStatement pst = con.prepareStatement(sql)) {
            pst.setInt(1, tripMap.get(cmbTrip.getSelectedItem().toString()));
            pst.setInt(2, destMap.get(cmbDest.getSelectedItem().toString()));
            pst.setString(3, txtArrival.getText());
            pst.setString(4, txtDeparture.getText());
            pst.setInt(5, Integer.parseInt(txtSeq.getText()));
            pst.setInt(6, oldTripId);
            pst.setInt(7, oldDestId);

            pst.executeUpdate();
            loadData();
            JOptionPane.showMessageDialog(this, "Το δρομολόγιο ενημερώθηκε!");
        } catch (Exception e) { JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            oldTripId = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            oldDestId = Integer.parseInt(jTable1.getValueAt(row, 1).toString());
            
            // Επιλογή στα ComboBoxes βάσει των IDs
            selectComboItemById(cmbTrip, tripMap, oldTripId);
            selectComboItemById(cmbDest, destMap, oldDestId);
            
            txtArrival.setText(jTable1.getValueAt(row, 2).toString());
            txtDeparture.setText(jTable1.getValueAt(row, 3).toString());
            txtSeq.setText(jTable1.getValueAt(row, 4).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       if (oldTripId == -1) return;
        try (Connection con = DBConnection.connect(); 
             PreparedStatement pst = con.prepareStatement("DELETE FROM travel_to WHERE to_tr_id=? AND to_dst_id=?")) {
            pst.setInt(1, oldTripId);
            pst.setInt(2, oldDestId);
            pst.executeUpdate();
            loadData();
            JOptionPane.showMessageDialog(this, "Η στάση διαγράφηκε!");
        } catch (Exception e) { JOptionPane.showMessageDialog(this, "Delete Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton4ActionPerformed

