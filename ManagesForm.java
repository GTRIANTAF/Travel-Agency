    private void fillAdminCombo() {
        cmbAdmin.removeAllItems();
        try (Connection con = DBConnection.connect();
             PreparedStatement pst = con.prepareStatement(
                 "SELECT a.adm_AT, w.wrk_name, w.wrk_lname FROM admin a JOIN worker w ON a.adm_AT = w.wrk_AT")) {
            
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                String fullName = rs.getString("wrk_name") + " " + rs.getString("wrk_lname");
                String at = rs.getString("adm_AT");
                adminMap.put(fullName, at);
                cmbAdmin.addItem(fullName);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void fillBranchCombo() {
        cmbBranch.removeAllItems();
        try (Connection con = DBConnection.connect();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT br_code, br_city, br_street FROM branch")) {
            
            while (rs.next()) {
                String branchDisplay = rs.getString("br_city") + " (" + rs.getString("br_street") + ")";
                String code = rs.getString("br_code");
                branchMap.put(branchDisplay, code);
                cmbBranch.addItem(branchDisplay);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void loadData(){                                        
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{"Admin AT", "Admin Name", "Branch City", "Branch Code"});

        try (Connection con = DBConnection.connect();
             PreparedStatement pst = con.prepareStatement(
                 "SELECT m.mng_adm_AT, w.wrk_name, w.wrk_lname, b.br_city, m.mng_br_code " +
                 "FROM manages m " +
                 "JOIN worker w ON m.mng_adm_AT = w.wrk_AT " +
                 "JOIN branch b ON m.mng_br_code = b.br_code")) {

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("mng_adm_AT"),
                    rs.getString("wrk_name") + " " + rs.getString("wrk_lname"),
                    rs.getString("br_city"),
                    rs.getString("mng_br_code")
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

