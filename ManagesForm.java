    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String adminAT = adminMap.get((String) cmbAdmin.getSelectedItem());
        String branchCode = branchMap.get((String) cmbBranch.getSelectedItem());

        try (Connection con = DBConnection.connect();
             PreparedStatement pst = con.prepareStatement(
                 "INSERT INTO manages (mng_adm_AT, mng_br_code) VALUES (?, ?)")) {
            
            pst.setString(1, adminAT);
            pst.setString(2, branchCode);
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Management assignment added!");
            loadData();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error (Possible duplicate): " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int row = jTable1.getSelectedRow();
        if (row != -1) {
            oldAdminAT = jTable1.getValueAt(row, 0).toString();
            oldBranchCode = jTable1.getValueAt(row, 3).toString();

            cmbAdmin.setSelectedItem(jTable1.getValueAt(row, 1).toString());

            String cityName = jTable1.getValueAt(row, 2).toString();
            for (int i = 0; i < cmbBranch.getItemCount(); i++) {
                if (cmbBranch.getItemAt(i).startsWith(cityName)) {
                    cmbBranch.setSelectedIndex(i);
                    break;
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) return;

        String adminAT = jTable1.getValueAt(row, 0).toString();
        String branchCode = jTable1.getValueAt(row, 3).toString();

        try (Connection con = DBConnection.connect();
             PreparedStatement pst = con.prepareStatement(
                 "DELETE FROM manages WHERE mng_adm_AT=? AND mng_br_code=?")) {
            
            pst.setString(1, adminAT);
            pst.setString(2, branchCode);
            pst.executeUpdate();
            loadData();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (oldAdminAT.equals("") || oldBranchCode.equals("")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a record first!");
        return;
    }

    String newAdminAT = adminMap.get((String) cmbAdmin.getSelectedItem());
    String newBranchCode = branchMap.get((String) cmbBranch.getSelectedItem());

    String sql = "UPDATE manages SET mng_adm_AT = ?, mng_br_code = ? " +
                 "WHERE mng_adm_AT = ? AND mng_br_code = ?";

    try (Connection con = DBConnection.connect();
         PreparedStatement pst = con.prepareStatement(sql)) {
        
        pst.setString(1, newAdminAT);
        pst.setString(2, newBranchCode);
        pst.setString(3, oldAdminAT);
        pst.setString(4, oldBranchCode);
        
        pst.executeUpdate();
         javax.swing.JOptionPane.showMessageDialog(this, "Assignment updated!");
        loadData();
    } catch (Exception e) {
         javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

