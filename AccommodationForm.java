    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
             javax.swing.JOptionPane.showMessageDialog(this, "Select a hotel to update.");
            return;
        }
        
        // Get ID from Column 0
        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        
        // Open Dialog in EDIT mode (pass ID)
        new AccommodationDialog(this, id).setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a hotel to delete.");
            return;
        }
        
        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        String name = jTable1.getValueAt(row, 1).toString();
        
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to delete '" + name + "'?", 
                "Delete Accommodation", 
                javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try (java.sql.Connection con = DBConnection.connect();
                 java.sql.PreparedStatement pst = con.prepareStatement("DELETE FROM accommodation WHERE acc_id=?")) {
                
                pst.setInt(1, id);
                pst.executeUpdate();
                
                javax.swing.JOptionPane.showMessageDialog(this, "Deleted Successfully!");
                loadData(); // Refresh table
                
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

