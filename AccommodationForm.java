    public void loadData() {                                         
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            // Define ALL 15 Columns
            model.setColumnIdentifiers(new Object[]{
                "ID", "Name", "Type", "Rating", "Stars", "Status", 
                "Street", "Num", "PostCode", "Dest ID", "Phone", 
                "Email", "Capacity", "Price", "Facilities"
            });

            try {
                java.sql.Connection con = DBConnection.connect();
                String sql = "SELECT * FROM accommodation"; 
                java.sql.PreparedStatement pst = con.prepareStatement(sql);
                java.sql.ResultSet rs = pst.executeQuery();

                while(rs.next()){
                    // Get all 15 columns from the database
                    String col1 = rs.getString("acc_id"); 
                    String col2 = rs.getString("acc_name"); 
                    String col3 = rs.getString("acc_type"); 
                    String col4 = rs.getString("acc_rating"); 
                    String col5 = rs.getString("acc_stars"); 
                    String col6 = rs.getString("acc_status"); 
                    String col7 = rs.getString("acc_street"); 
                    String col8 = rs.getString("acc_street_num"); 
                    String col9 = rs.getString("acc_postcode"); 
                    String col10 = rs.getString("acc_dst_id"); 
                    String col11 = rs.getString("acc_phone"); 
                    String col12 = rs.getString("acc_email"); 
                    String col13 = rs.getString("acc_roomcapacity"); 
                    String col14 = rs.getString("acc_pricepernight"); 
                    String col15 = rs.getString("acc_facilities");

                    // Add the row with all 15 values
                    model.addRow(new Object[]{
                        col1, col2, col3, col4, col5, col6, col7, 
                        col8, col9, col10, col11, col12, col13, col14, col15
                    });
                }
                con.close(); 
            } catch (Exception e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    
