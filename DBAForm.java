    private void fillAdminCombo() {
    cmbAdmin.removeAllItems();
    try (Connection con = DBConnection.connect();
         Statement st = con.createStatement();
         ResultSet rs = st.executeQuery("SELECT adm_AT FROM admin")) {
        while (rs.next()) {
            String at = rs.getString("adm_AT");
            adminMap.put(at, at);
            cmbAdmin.addItem(at);
        }
    } catch (Exception e) { e.printStackTrace(); }
    }
    
    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{"ID", "Admin AT", "Start Date", "End Date"});

        try (Connection con = DBConnection.connect();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT * FROM dba ORDER BY dba_id DESC")) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("dba_id"), 
                    rs.getString("dba_admin_AT"), 
                    rs.getTimestamp("dba_start_date"), 
                    rs.getTimestamp("dba_end_date")
                });
            }
        } catch (Exception e) { JOptionPane.showMessageDialog(this, "Error: " + e.getMessage()); }
    }
    
