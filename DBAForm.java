    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    String sql = "INSERT INTO dba (dba_admin_AT, dba_start_date, dba_end_date) VALUES (?, ?, ?)";
    try (Connection con = DBConnection.connect(); PreparedStatement pst = con.prepareStatement(sql)) {
        pst.setString(1, cmbAdmin.getSelectedItem().toString());
        pst.setString(2, txtStart.getText()); // Format: YYYY-MM-DD HH:MM:SS
        
        if (txtEnd.getText().isEmpty()) pst.setNull(3, java.sql.Types.TIMESTAMP);
        else pst.setString(3, txtEnd.getText());

        pst.executeUpdate();
        loadData();
        JOptionPane.showMessageDialog(this, "Επιτυχής καταχώρηση DBA!");
    } catch (Exception e) { JOptionPane.showMessageDialog(this, "Insert Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    if (txtID.getText().isEmpty()) return;
    try (Connection con = DBConnection.connect(); 
         PreparedStatement pst = con.prepareStatement("DELETE FROM dba WHERE dba_id=?")) {
        pst.setInt(1, Integer.parseInt(txtID.getText()));
        pst.executeUpdate();
        loadData();
    } catch (Exception e) { JOptionPane.showMessageDialog(this, "Delete Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    if (txtID.getText().isEmpty()) return;
    String sql = "UPDATE dba SET dba_admin_AT=?, dba_start_date=?, dba_end_date=? WHERE dba_id=?";
    try (Connection con = DBConnection.connect(); PreparedStatement pst = con.prepareStatement(sql)) {
        pst.setString(1, cmbAdmin.getSelectedItem().toString());
        pst.setString(2, txtStart.getText());
        pst.setString(3, txtEnd.getText().isEmpty() ? null : txtEnd.getText());
        pst.setInt(4, Integer.parseInt(txtID.getText()));
        
        pst.executeUpdate();
        loadData();
    } catch (Exception e) { JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
    int row = jTable1.getSelectedRow();
    if (row != -1) {
        txtID.setText(jTable1.getValueAt(row, 0).toString());
        cmbAdmin.setSelectedItem(jTable1.getValueAt(row, 1).toString());
        txtStart.setText(jTable1.getValueAt(row, 2).toString());
        // Χειρισμός NULL για το End Date
        Object endDate = jTable1.getValueAt(row, 3);
        txtEnd.setText(endDate == null ? "" : endDate.toString());
    }
    }//GEN-LAST:event_jTable1MouseClicked

