    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
     int row = jTable1.getSelectedRow();
        if (row != -1) {
            txtId.setText(jTable1.getValueAt(row, 0).toString());
            txtBrand.setText(jTable1.getValueAt(row, 1).toString());
            txtModel.setText(jTable1.getValueAt(row, 2).toString());
            txtPlate.setText(jTable1.getValueAt(row, 3).toString());
            txtSeats.setText(jTable1.getValueAt(row, 4).toString());
            cmbType.setSelectedItem(jTable1.getValueAt(row, 5).toString());
            cmbStatus.setSelectedItem(jTable1.getValueAt(row, 6).toString());
            txtKM.setText(jTable1.getValueAt(row, 7).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String id = txtId.getText(); // ID fetched from table click
        if(id.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a vehicle from the table to delete.");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement("DELETE FROM vehicle WHERE veh_id=?")) {
            pst.setInt(1, Integer.parseInt(id));
            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Vehicle Deleted!");
            loadData();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Delete Error (Check constraints): " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

