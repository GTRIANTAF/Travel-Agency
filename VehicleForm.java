    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String brand = txtBrand.getText().trim();
        String model = txtModel.getText().trim();
        String plate = txtPlate.getText().trim();
        int seats = Integer.parseInt(txtSeats.getText().trim());
        String type = cmbType.getSelectedItem().toString();
        String status = cmbStatus.getSelectedItem().toString();
        int km = txtKM.getText().isEmpty() ? 0 : Integer.parseInt(txtKM.getText().trim());

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "INSERT INTO vehicle (veh_brand, veh_model, veh_plate, veh_seats, veh_type, veh_status, veh_total_km) VALUES (?, ?, ?, ?, ?, ?, ?)")) {
            
            pst.setString(1, brand);
            pst.setString(2, model);
            pst.setString(3, plate);
            pst.setInt(4, seats);
            pst.setString(5, type);
            pst.setString(6, status);
            pst.setInt(7, km);

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Vehicle added to fleet!");
            loadData();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = txtId.getText().trim();
        if(id.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a vehicle to update.");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "UPDATE vehicle SET veh_brand=?, veh_model=?, veh_plate=?, veh_seats=?, veh_type=?, veh_status=?, veh_total_km=? WHERE veh_id=?")) {
            
            pst.setString(1, txtBrand.getText());
            pst.setString(2, txtModel.getText());
            pst.setString(3, txtPlate.getText());
            pst.setInt(4, Integer.parseInt(txtSeats.getText()));
            pst.setString(5, cmbType.getSelectedItem().toString());
            pst.setString(6, cmbStatus.getSelectedItem().toString());
            pst.setInt(7, Integer.parseInt(txtKM.getText()));
            pst.setInt(8, Integer.parseInt(id));

            pst.executeUpdate();
            javax.swing.JOptionPane.showMessageDialog(this, "Vehicle Updated!");
            loadData();
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Update Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

