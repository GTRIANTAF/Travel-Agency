    private void loadData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        model.setColumnIdentifiers(new Object[]{
            "ID", "Brand", "Model", "Plate", "Seats", "Type", "Status", "Total KM"
        });

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement("SELECT * FROM vehicle");
             java.sql.ResultSet rs = pst.executeQuery()) {

            while(rs.next()){
                model.addRow(new Object[]{
                    rs.getInt("veh_id"),
                    rs.getString("veh_brand"),
                    rs.getString("veh_model"),
                    rs.getString("veh_plate"),
                    rs.getInt("veh_seats"),
                    rs.getString("veh_type"),
                    rs.getString("veh_status"),
                    rs.getInt("veh_total_km")
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

