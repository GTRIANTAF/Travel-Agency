    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{"ID", "Trip ID", "Hotel ID", "Check-In", "Check-Out", "Rooms", "Cost"});

        String sql = "SELECT * FROM trip_accommodation ORDER BY ta_check_in DESC";
        try (Connection con = DBConnection.connect(); Statement st = con.createStatement(); ResultSet rs = st.executeQuery(sql)) {
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("ta_id"), rs.getInt("ta_trip_id"), rs.getInt("ta_acc_id"),
                    rs.getTimestamp("ta_check_in"), rs.getTimestamp("ta_check_out"),
                    rs.getInt("ta_rooms"), rs.getBigDecimal("ta_total_cost")
                });
            }
        } catch (Exception e) { JOptionPane.showMessageDialog(this, "Load Error: " + e.getMessage()); }
    }
    
    private void fillCombos() {
    try (Connection con = DBConnection.connect(); Statement st = con.createStatement()) {
        // Trips
        cmbTrip.removeAllItems();
        ResultSet rs1 = st.executeQuery("SELECT tr_id, tr_departure FROM trip");
        while (rs1.next()) {
            String label = "Trip #" + rs1.getInt("tr_id") + " (" + rs1.getTimestamp("tr_departure") + ")";
            tripMap.put(label, rs1.getInt("tr_id"));
            cmbTrip.addItem(label);
        }

        // Accommodations/Hotels
        cmbAcc.removeAllItems();
        ResultSet rs2 = st.executeQuery("SELECT acc_id, acc_name FROM accommodation");
        while (rs2.next()) {
            String name = rs2.getString("acc_name");
            accMap.put(name, rs2.getInt("acc_id"));
            cmbAcc.addItem(name);
        }
        } catch (Exception e) { e.printStackTrace(); }
    }
    
    private void selectComboItem(JComboBox combo, HashMap<String, Integer> map, int id) {
        for (String label : map.keySet()) {
            if (map.get(label) == id) {
                combo.setSelectedItem(label);
                break;
            }
        }
    }
    
