        if (row != -1) {
            txtID.setText(jTable1.getValueAt(row, 0).toString());
        
        selectComboItem(cmbTrip, tripMap, (int)jTable1.getValueAt(row, 1));
        selectComboItem(cmbAcc, accMap, (int)jTable1.getValueAt(row, 2));
        
        txtCheckIn.setText(jTable1.getValueAt(row, 3).toString());
        txtCheckOut.setText(jTable1.getValueAt(row, 4).toString());
        txtRooms.setText(jTable1.getValueAt(row, 5).toString());
        txtCost.setText(jTable1.getValueAt(row, 6).toString());
    }
