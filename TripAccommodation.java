    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String sql = "INSERT INTO trip_accommodation (ta_trip_id, ta_acc_id, ta_check_in, ta_check_out, ta_rooms, ta_total_cost) VALUES (?,?,?,?,?,?)";
        try (Connection con = DBConnection.connect(); PreparedStatement pst = con.prepareStatement(sql)) {
        pst.setInt(1, tripMap.get(cmbTrip.getSelectedItem().toString()));
        pst.setInt(2, accMap.get(cmbAcc.getSelectedItem().toString()));
        pst.setString(3, txtCheckIn.getText()); // YYYY-MM-DD HH:MM:SS
        pst.setString(4, txtCheckOut.getText());
        pst.setInt(5, Integer.parseInt(txtRooms.getText()));
        pst.setDouble(6, Double.parseDouble(txtCost.getText()));

        pst.executeUpdate();
        loadData();
        JOptionPane.showMessageDialog(this, "Η διαμονή καταχωρήθηκε!");
    } catch (Exception e) { JOptionPane.showMessageDialog(this, "Insert Error: " + e.getMessage()); }
    }//GEN-LAST:event_jButton2ActionPerformed

