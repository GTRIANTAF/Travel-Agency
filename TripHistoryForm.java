    private void loadHistoryData(String sql) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{
            "Hist. ID", "Trip ID", "Departure", "Return", "Destinations", "Participants", "Revenue"
        });

        try (Connection con = DBConnection.connect();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery(sql)) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("th_id"),
                    rs.getInt("th_trip_id"),
                    rs.getTimestamp("th_departure"),
                    rs.getTimestamp("th_return"),
                    rs.getInt("th_destinations_count"),
                    rs.getInt("th_participants_count"),
                    rs.getBigDecimal("th_total_revenue")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading history: " + e.getMessage());
        }
    }

