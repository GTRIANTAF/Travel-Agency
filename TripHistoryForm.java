    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
       String tripId = txtTripID.getText().trim();
        String dateFrom = txtDep.getText().trim();

        String sql = "SELECT * FROM trip_history WHERE 1=1";
        if (!tripId.isEmpty()) sql += " AND th_trip_id = " + tripId;
        if (!dateFrom.isEmpty()) sql += " AND th_departure >= '" + dateFrom + " 00:00:00'";
        
        sql += " ORDER BY th_departure DESC LIMIT 1000";
        loadHistoryData(sql);
    }//GEN-LAST:event_searchActionPerformed

