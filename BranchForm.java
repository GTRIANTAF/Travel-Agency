    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String street = txtStreet.getText();
        String num = txtNumber.getText();
        String city = txtCity.getText();
        String manager = txtManager.getText(); 
        
        if(street.isEmpty() || city.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill Street and City!");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "INSERT INTO branch (br_street, br_num, br_city, br_manager_AT) VALUES (?, ?, ?, ?)")) {
            
            pst.setString(1, street);
            
            if(num.isEmpty()) pst.setNull(2, java.sql.Types.INTEGER);
            else pst.setInt(2, Integer.parseInt(num));
            
            pst.setString(3, city);
            
            if(manager.isEmpty()) pst.setNull(4, java.sql.Types.VARCHAR);
            else pst.setString(4, manager);
            
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Branch Added!");
            loadData();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String id = txtId.getText();
        String street = txtStreet.getText();
        String num = txtNumber.getText();
        String city = txtCity.getText();
        String manager = txtManager.getText();
        
        if(id.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a branch to update.");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "UPDATE branch SET br_street=?, br_num=?, br_city=?, br_manager_AT=? WHERE br_code=?")) {
            
            pst.setString(1, street);
            
            if(num.isEmpty()) pst.setNull(2, java.sql.Types.INTEGER);
            else pst.setInt(2, Integer.parseInt(num));
            
            pst.setString(3, city);
            
            if(manager.isEmpty()) pst.setNull(4, java.sql.Types.VARCHAR);
            else pst.setString(4, manager);
            
            pst.setInt(5, Integer.parseInt(id));
            
            int rows = pst.executeUpdate();
            
            if (rows > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Updated!");
                loadData();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "ID not found!");
            }
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
int row = jTable1.getSelectedRow();
        if (row != -1) {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            
            // Map table columns to your specific TextFields
            txtId.setText(model.getValueAt(row, 0).toString());
            txtStreet.setText(model.getValueAt(row, 1).toString());
            txtNumber.setText(model.getValueAt(row, 2).toString());
            txtCity.setText(model.getValueAt(row, 3).toString());
            
            Object mgr = model.getValueAt(row, 4);
            txtManager.setText(mgr != null ? mgr.toString() : "");
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
String id = txtId.getText();
        
        if(id.isEmpty()) {
             javax.swing.JOptionPane.showMessageDialog(this, "Select a branch to delete.");
             return;
        }
        
        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement("DELETE FROM branch WHERE br_code=?")) {
            
            pst.setInt(1, Integer.parseInt(id));
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Deleted!");
            loadData();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

