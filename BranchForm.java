    private void loadData(){                                         
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        model.setColumnIdentifiers(new Object[]{"Code", "Street", "Number", "City", "Manager AT"});

        try {
            java.sql.Connection con = DBConnection.connect();
            String sql = "SELECT * FROM branch"; 
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String col1 = rs.getString("br_code"); 
                String col2 = rs.getString("br_street"); 
                String col3 = rs.getString("br_num"); 
                String col4 = rs.getString("br_city"); 
                String col5 = rs.getString("br_manager_AT"); 
                
                model.addRow(new Object[]{col1, col2, col3, col4, col5});
            }
            con.close(); 
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
