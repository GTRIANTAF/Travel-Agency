    private void loadData(){                                         
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        
        model.setColumnIdentifiers(new Object[]{"Driver AT", "License", "Route", "Experience"});

        try {
            java.sql.Connection con = DBConnection.connect();
            String sql = "SELECT * FROM driver"; 
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            java.sql.ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String col1 = rs.getString("drv_AT"); 
                String col2 = rs.getString("drv_license"); 
                String col3 = rs.getString("drv_route"); 
                String col4 = rs.getString("drv_experience"); 
                
                model.addRow(new Object[]{col1, col2, col3, col4});
            }
            con.close(); 
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

