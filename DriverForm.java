    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
String at = txtAT.getText().trim();
        String license = cmbLicense.getSelectedItem().toString(); // Get from Combo
        String route = cmbRoute.getSelectedItem().toString();     // Get from Combo
        String exp = txtEx.getText().trim();
        
        if(at.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Please fill Driver AT!");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "INSERT INTO driver (drv_AT, drv_license, drv_route, drv_experience) VALUES (?, ?, ?, ?)")) {
            
            pst.setString(1, at);
            pst.setString(2, license);
            pst.setString(3, route);
            
            // Handle Experience (Integer/Null)
            if(exp.isEmpty()) {
                pst.setNull(4, java.sql.Types.TINYINT);
            } else {
                try {
                    pst.setInt(4, Integer.parseInt(exp));
                } catch (NumberFormatException ex) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Experience must be a number!");
                    return;
                }
            }
            
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Driver Added!");
            loadData();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String at = txtAT.getText().trim();
        String license = cmbLicense.getSelectedItem().toString();
        String route = cmbRoute.getSelectedItem().toString();
        String exp = txtEx.getText().trim();
        
        if(at.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Select a driver to update.");
            return;
        }

        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement(
             "UPDATE driver SET drv_license=?, drv_route=?, drv_experience=? WHERE drv_AT=?")) {
            
            pst.setString(1, license);
            pst.setString(2, route);
            
            if(exp.isEmpty()) {
                pst.setNull(3, java.sql.Types.TINYINT);
            } else {
                 try {
                    pst.setInt(3, Integer.parseInt(exp));
                } catch (NumberFormatException ex) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Experience must be a number!");
                    return;
                }
            }
            
            pst.setString(4, at);
            
            int rows = pst.executeUpdate();
            
            if (rows > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Updated!");
                loadData();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Driver AT not found!");
            }
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
int row = jTable1.getSelectedRow();
        if (row != -1) {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            
            txtAT.setText(model.getValueAt(row, 0).toString());
            
            // Set Dropdowns
            String license = model.getValueAt(row, 1).toString();
            cmbLicense.setSelectedItem(license);
            
            String route = model.getValueAt(row, 2).toString();
            cmbRoute.setSelectedItem(route);
            
            // Set Experience
            Object exp = model.getValueAt(row, 3);
            txtEx.setText(exp != null ? exp.toString() : "");
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
String at = txtAT.getText();
        
        if(at.isEmpty()) {
             javax.swing.JOptionPane.showMessageDialog(this, "Select a driver to delete.");
             return;
        }
        
        try (java.sql.Connection con = DBConnection.connect();
             java.sql.PreparedStatement pst = con.prepareStatement("DELETE FROM driver WHERE drv_AT=?")) {
            
            pst.setString(1, at);
            pst.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Deleted!");
            loadData();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

